DROP TABLE IF EXISTS comissoes, itens_venda, vendas, estoque, produtos, vendedores, clientes, formas_pagamento, cores, marcas, categoria, tempo CASCADE;

CREATE TABLE categorias (id SERIAL PRIMARY KEY, nome VARCHAR(100) NOT NULL);
CREATE TABLE marcas (id SERIAL PRIMARY KEY, nome VARCHAR(100) NOT NULL);
CREATE TABLE cores (id SERIAL PRIMARY KEY, nome VARCHAR(50) NOT NULL);
CREATE TABLE formas_pagamento (id SERIAL PRIMARY KEY, descricao VARCHAR(100) NOT NULL);

CREATE TABLE clientes (
    id SERIAL PRIMARY KEY,
    nome VARCHAR(150) NOT NULL,
    tipo VARCHAR(2) NOT NULL,
    documento VARCHAR(20) NOT NULL,
    email VARCHAR(150),
    telefone VARCHAR(20),
    endereco TEXT
);

CREATE TABLE vendedores (
    id SERIAL PRIMARY KEY,
    nome VARCHAR(150) NOT NULL,
    email VARCHAR(100),
    telefone VARCHAR(20)
);

CREATE TABLE produtos (
    id SERIAL PRIMARY KEY,
    nome VARCHAR(150) NOT NULL,
    preco NUMERIC(10,2) NOT NULL,
    categoria_id INT REFERENCES categorias(id),
    marca_id INT REFERENCES marcas(id),
    cor_id INT REFERENCES cores(id),
    ativo BOOLEAN DEFAULT TRUE
);

CREATE TABLE estoque (
    id SERIAL PRIMARY KEY,
    produto_id INT,
    quantidade INT NOT NULL,
    data_atualizacao TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE vendas (
    id SERIAL PRIMARY KEY,
    cliente_id INT,
    vendedor_id INT,
    data_venda TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
    forma_pagamento_id INT,
    valor_total NUMERIC(10,2),
    desconto NUMERIC(10,2) DEFAULT 0,
    imposto NUMERIC(10,2) DEFAULT 0
);

CREATE TABLE itens_venda (
    id SERIAL PRIMARY KEY,
    venda_id INT,
    produto_id INT,
    quantidade INT NOT NULL,
    preco_unitario NUMERIC(10,2) NOT NULL,
    total_item NUMERIC(10,2) NOT NULL
);

CREATE TABLE comissoes (
    id SERIAL PRIMARY KEY,
    vendedor_id INT,
    venda_id INT,
    percentual NUMERIC(5,2) NOT NULL,
    valor NUMERIC(10,2) NOT NULL
);

CREATE TABLE tempo (
    data DATE PRIMARY KEY,
    ano INT,
    mes INT,
    dia INT,
    dia_semana INT,
    nome_dia_semana VARCHAR(15),
    trimestre INT,
    semana_ano INT,
    feriado BOOLEAN,
    dia_util BOOLEAN
);

